Msg* Rewinder::ProcessMsg(MsgEncodedStream* aMsg)
{
    iBuffering = true;
    // clear data in case previous stream was exhausted while buffering
    DrainFifo(*iFifoCurrent);
    DrainFifo(*iFifoNext);
    return aMsg;
}

